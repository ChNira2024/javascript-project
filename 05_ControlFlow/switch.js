const day = 3;
switch (day) {
  case 1:
    console.log("Monday");
    break;
  case 2:
    console.log("Tuesday");
    break;
  case 3:
    console.log("Wednesday");
    break;
  default:
    console.log("Invalid day");
}

const x = 2;
switch (x) {
  case 1:
    console.log("One");
  case 2:
    console.log("Two");
  case 3:
    console.log("Three");
}

const fruitType = "banana";
switch (fruitType) {
  case "apple":
    console.log("Apples are $2 per kg.");
    break;
  case "banana":
    console.log("Bananas are $1 per kg.");
    break;
  case "orange":
    console.log("Oranges are $1.5 per kg.");
    break;
  default:
    console.log("Sorry, we don't have that fruit");
    break;
}
